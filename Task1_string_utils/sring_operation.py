def reverse_string(s):
    reverse_string_for_return = s[::-1]
    print(f"The reversed string is: {reverse_string_for_return}")
    return reverse_string_for_return


def capitalize_words(s):
    capitalize_words_for_return = s.title()
    print(f"The string with capitalize words is: {capitalize_words_for_return}")
    return capitalize_words_for_return
